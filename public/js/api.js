// Modal helper
function showModal(title, bodyHtml, actions = []) {
  const root = document.getElementById('modal-root');
  const overlay = document.createElement('div');
  overlay.className = 'modal-overlay';
  overlay.innerHTML = `
    <div class="modal">
      <div class="modal-header">
        <div class="modal-title">${title}</div>
        <button class="btn-icon modal-close"><svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M18 6 6 18"/><path d="m6 6 12 12"/></svg></button>
      </div>
      <div class="modal-body">${bodyHtml}</div>
      <div class="modal-footer" id="modal-footer"></div>
    </div>
  `;

  const footer = overlay.querySelector('#modal-footer');
  actions.forEach(a => {
    const btn = document.createElement('button');
    btn.className = a.className || 'btn btn-secondary';
    btn.textContent = a.label;
    btn.onclick = () => { if (a.onClick) a.onClick(); closeModal(); };
    footer.appendChild(btn);
  });

  overlay.querySelector('.modal-close').onclick = closeModal;
  overlay.onclick = (e) => { if (e.target === overlay) closeModal(); };

  function closeModal() { overlay.remove(); }
  root.appendChild(overlay);
  return { close: closeModal, overlay };
}

// Confirm dialog
function showConfirm(title, message, onConfirm, danger = false) {
  return showModal(title, `<p style="color: var(--text-secondary)">${message}</p>`, [
    { label: 'Cancel', className: 'btn btn-secondary' },
    { label: 'Confirm', className: danger ? 'btn btn-danger' : 'btn btn-primary', onClick: onConfirm },
  ]);
}

// Utility functions
function formatBytes(bytes, decimals = 1) {
  if (bytes === 0) return '0 B';
  const k = 1024;
  const sizes = ['B', 'KB', 'MB', 'GB', 'TB'];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  return parseFloat((bytes / Math.pow(k, i)).toFixed(decimals)) + ' ' + sizes[i];
}

function timeAgo(timestamp) {
  const seconds = typeof timestamp === 'number'
    ? Math.floor(Date.now() / 1000 - timestamp)
    : Math.floor((Date.now() - new Date(timestamp).getTime()) / 1000);
  if (seconds < 60) return 'just now';
  if (seconds < 3600) return Math.floor(seconds / 60) + 'm ago';
  if (seconds < 86400) return Math.floor(seconds / 3600) + 'h ago';
  if (seconds < 604800) return Math.floor(seconds / 86400) + 'd ago';
  return new Date(typeof timestamp === 'number' ? timestamp * 1000 : timestamp).toLocaleDateString();
}

function escapeHtml(str) {
  const div = document.createElement('div');
  div.textContent = str;
  return div.innerHTML;
}

function syntaxHighlightJSON(json) {
  if (typeof json !== 'string') json = JSON.stringify(json, null, 2);
  return json.replace(/("(\\u[a-zA-Z0-9]{4}|\\[^u]|[^\\"])*"(\s*:)?|\b(true|false|null)\b|-?\d+(?:\.\d*)?(?:[eE][+\-]?\d+)?)/g, (match) => {
    let cls = 'json-number';
    if (/^"/.test(match)) { cls = /:$/.test(match) ? 'json-key' : 'json-string'; }
    else if (/true|false/.test(match)) cls = 'json-boolean';
    else if (/null/.test(match)) cls = 'json-null';
    return `<span class="${cls}">${match}</span>`;
  });
}

